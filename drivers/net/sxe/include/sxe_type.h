typedef union sxe_adv_tx_desc {
	struct {
		U64 buffer_addr;
		U32 cmd_type_len;
		U32 olinfo_status;
	} read;
	struct {
		U64 rsvd;
		U32 nxtseq_seed;
		U32 status;
	} wb;
} sxe_adv_tx_desc_u;

typedef union sxe_adv_rx_desc {
	struct {
		U64 pkt_addr;
		U64 hdr_addr;
	} read;
	struct {
		struct {
			union {
				U32 data;
				struct {
					U16 pkt_info;
					U16 hdr_info;
				} hs_rss;
			} lo_dword;
			union {
				U32 rss;
				struct {
					U16 ip_id;
					U16 csum;
				} csum_ip;
			} hi_dword;
		} lower;
		struct {
			U32 status_error;
			U16 length;
			U16 vlan;
		} upper;
	} wb;
} sxe_adv_rx_desc_u;

